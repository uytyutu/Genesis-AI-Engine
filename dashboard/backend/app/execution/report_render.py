"""Professional HTML rendering for business conclusions — no raw Markdown in browser."""

from __future__ import annotations

import html
import re


def _inline(text: str) -> str:
    escaped = html.escape(text)
    return re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", escaped)


def markdown_to_simple_html(md: str) -> str:
    """Minimal Markdown → HTML for controlled report templates."""
    out: list[str] = []
    in_ul = False

    def close_ul() -> None:
        nonlocal in_ul
        if in_ul:
            out.append("</ul>")
            in_ul = False

    for raw in md.splitlines():
        line = raw.rstrip()
        if not line.strip():
            close_ul()
            continue
        if line.startswith("# "):
            close_ul()
            out.append(f"<h1>{_inline(line[2:])}</h1>")
        elif line.startswith("## "):
            close_ul()
            out.append(f"<h2>{_inline(line[3:])}</h2>")
        elif line.startswith("### "):
            close_ul()
            out.append(f"<h3>{_inline(line[4:])}</h3>")
        elif line.startswith("---"):
            close_ul()
            out.append("<hr/>")
        elif line.startswith("- "):
            if not in_ul:
                out.append("<ul>")
                in_ul = True
            out.append(f"<li>{_inline(line[2:])}</li>")
        elif line.startswith("> "):
            close_ul()
            out.append(f"<blockquote>{_inline(line[2:])}</blockquote>")
        else:
            close_ul()
            out.append(f"<p>{_inline(line)}</p>")

    close_ul()
    return "\n".join(out)


