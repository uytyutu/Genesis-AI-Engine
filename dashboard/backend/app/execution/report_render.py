def wrap_report_html(body_html: str, *, title: str = "Business Conclusion") -> str:
    safe_title = html.escape(title)
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8"/>
  <meta name="viewport" content="width=device-width, initial-scale=1"/>
  <title>{safe_title} — Vector</title>
  <style>{_VIRTUS_REPORT_CSS}</style>
</head>
<body>
  <div class="wrap">
    <div class="brand">Vector · Virtus Core</div>
    {body_html}
    <div class="footer">Professional business conclusion — generated by Vector execution layer.</div>
  </div>
</body>
</html>"""


def render_report_html_from_markdown(md: str, *, title: str = "Business Conclusion") -> str:
    return wrap_report_html(markdown_to_simple_html(md), title=title)
